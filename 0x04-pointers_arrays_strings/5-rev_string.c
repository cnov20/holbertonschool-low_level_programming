/**
 * rev_string - reverses a given string
 * @s: string to be reversed
 *
 * Return: (0)
 */

void rev_string(char *s)
{
        int i;
        int j;

        i = 0;
        while (s[i] != '\0')
        {
                i++;
        }
        i -= 1;
        j = i;
        while (j >= 0)
        {
                j--;

        }
}
